// Utility functions
export const getAllPaintings = (): Painting[] => {
  return paintings;
};

export const getPaintingById = (id: string): Painting | undefined => {
  return paintings.find(painting => painting.id === id);
};

export const getPaintingBySlug = (slug: string): Painting | undefined => {
  return paintings.find(painting => painting.slug === slug);
};

export const getPaintingsByArtist = (artistId: string): Painting[] => {
  return paintings.filter(painting => painting.artist_id === artistId);
};

export const getPaintingsByMovement = (movement: string): Painting[] => {
  return paintings.filter(painting => painting.movement?.toLowerCase().includes(movement.toLowerCase()));
};

export const getLatestPaintings = (count: number = 3): Painting[] => {
  // Sort by creation date (newest first) and take the requested number
  return [...paintings]
    .sort((a, b) => {
      const dateA = a.creation_date || a.creation_year || '';
      const dateB = b.creation_date || b.creation_year || '';
      return dateB.localeCompare(dateA);
    })
    .slice(0, count);
};
